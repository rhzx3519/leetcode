func sampleStats(count []int) []float64 {
	var maxVal, minVal = -1, 0xffff
	var mode, freq int
	var n, s int
	var mapper = make(map[int]int)
	for i := range count {
		if count[i] == 0 {
			continue
		}
		mapper[i] = count[i]
		n += count[i]
		maxVal = max(maxVal, i)
		minVal = min(minVal, i)
		s += i*count[i]
		if count[i] > freq {
			freq = count[i]
			mode = i
		}
	}
	fmt.Println(mapper)
	var i1, i2 int
	if n&1 == 1 {
		i1 = n>>1 + 1
		i2 = i1
	} else {
		i1 = n >> 1
		i2 = n>>1 + 1
	}
	var x1, x2 = -1, -1

	var k int
	for i := range count {
		if count[i] == 0 {
			continue
		}
		k += count[i]
		if x1 == -1 && k >= i1 {
			x1 = i
		}
		if x2 == -1 && k >= i2 {
			x2 = i
		}

		if x1 != -1 && x2 != -1 {
			break
		}
	}

	var mid = float64(x1 + x2) / float64(2)
	return []float64{float64(minVal), float64(maxVal), float64(s)/float64(n), mid, float64(mode)}
}


func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

